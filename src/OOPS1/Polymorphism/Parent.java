package OOPS1.Polymorphism;

 class Parent {
    void print(){
        System.out.println("Parent Class");
    }
}

 class Subclass extends Parent{
     void print(){
         System.out.println("subclass1 Class");
     }
}
class Subclass2 extends Parent{
    void print(){
        System.out.println("subclass2 Class");
    }
}

